// Напишите код, который посчитает сумму всех парных элементов в массиве.
// Использовать встроенные методы массивов — нельзя.

////////////////// Задание //////////////////
// const array = [1, 2, 3, 4];

////////////////// Решение //////////////////

const array = [1, 2, 3, 4];
let summa  = 0;
let curNum;

for (curNum of array) {
    if (curNum % 2 === 0) {
        summa = summa + curNum;
    }
}
console.log(summa);

