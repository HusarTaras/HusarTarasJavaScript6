// Напишите код, который посчитает сумму всех парных элементов в массиве.
// Использовать встроенные методы массивов — нельзя.

////////////////// Задание //////////////////
// const array = [1, 2, 3, 4];

////////////////// Решение //////////////////

const array = [1, 2, 3, 4];
var summa  = 0;

for (CurNum of array) {
    if (CurNum % 2 === 0) {
        summa = summa + CurNum;
    }
}
console.log(summa);

