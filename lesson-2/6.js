// Написать код который посчитает сумму всех элементов в массиве.
// Использовать встроенные методы массивов — нельзя.

////////////////// Задание //////////////////
// const array = [1, 2, 3, 4];

////////////////// Решение //////////////////

const array = [1, 2, 3, 4];
let summa  = 0;
let curNum;

for (curNum of array) {
    summa = summa + curNum;
}
console.log(summa);
